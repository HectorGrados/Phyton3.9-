"""
Funciones integradas en python
"""
#Funcion round
numero= round(4.8)
print(numero)
#Funcion round con parametro
numero= round(4.845542,2)
print(numero)
#Funcion eval
numero=eval('2+5')
print(numero)
#Funcion abs = retorna valor absoluto
numero=abs(-20)
print(numero)
#Funcion bin = retorna numero en binario
numero=bin(10)
print(numero)
#Funcion int = retorna binario a entero
numero=int(0b1010)
print(numero)
#Funcion hex = retorna a hexadecimal
numero=hex(10)
print(numero) 
